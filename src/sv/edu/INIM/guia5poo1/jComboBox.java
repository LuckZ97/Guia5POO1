    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        String prueba;
        prueba = (String)this.jComboBox1.getSelectedItem();
        
        if(prueba.equals("Opcion1")){
            lblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sv/edu/INIM/guia5poo1/img/Desert.jpg")));
        }
        if(prueba.equals("Opcion2")){
            lblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sv/edu/INIM/guia5poo1/img/Hydrangeas.jpg")));
        }
        if(prueba.equals("Opcion3")){
            lblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sv/edu/INIM/guia5poo1/img/Lighthouse.jpg")));
        }
        if(prueba.equals("Opcion...")){
            lblImagen.setIcon(null);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

