    private void btnRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarActionPerformed
        // TODO add your handling code here:
        int n1 = Integer.valueOf(txtPNumero.getText());
        int n2 = Integer.valueOf(txtSNumero.getText());
        int resultado;
        String operacion = (String)this.jComboBox1.getSelectedItem();
        
        switch (operacion) {
            case "Suma":
                lblPNumero.setText("Sumando:");
                lblSNumero.setText("Sumando:");
                resultado = n1 + n2;
                lblResultado.setText("Total:" + resultado);
                break;
            case "Resta":
                lblPNumero.setText("Minuendo:");
                lblSNumero.setText("Sustraendo:");
                resultado = n1 - n2;
                lblResultado.setText("Diferencia:" + resultado);
                break;
            case "Divisi贸n":
                lblPNumero.setText("Dividendo:");
                lblSNumero.setText("Divisor:");
                resultado = n1 / n2;
                lblResultado.setText("Cociente: " + resultado);
                break;
            case "Multiplicaci贸n":
                lblPNumero.setText("Multiplicando:");
                lblSNumero.setText("Multiplicador:");
                resultado = n1 * n2;
                lblResultado.setText("Producto: " + resultado);
                break;
            default:
                lblPNumero.setText("Primer Numero:");
                lblSNumero.setText("Segundo Numero");
        }
    }//GEN-LAST:event_btnRealizarActionPerformed

